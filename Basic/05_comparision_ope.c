#include <stdio.h>

int main()
{
    // comparision operator
    int n1 = 20, n2 = 30;
    printf("n1 is greater than n2 : %d\n", n1 > n2);
    printf("n1 is less than  n2 : %d\n", n1 < n2);
    printf("n1 is equal to n2 : %d\n", n1 == n2);
    printf("n1 is greater than equal to n2 : %d\n", n1 >= n2);
    printf("n1 is less than equal to n2 : %d\n", n1 <= n2);

    return 0;
}